package excepciones;

public class NonExistingProductException extends Exception {

	private static final long serialVersionUID = 1L;
	
	public NonExistingProductException() {
		super();
	}
	
	public NonExistingProductException(Throwable t) {
		super();
	}
	
	public NonExistingProductException(Throwable t, String message) {
		super();
	}
	
	public NonExistingProductException(String message) {
		super();
	}

}
