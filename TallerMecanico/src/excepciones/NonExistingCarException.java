package excepciones;

public class NonExistingCarException extends Exception {
	
	private static final long serialVersionUID = 1L;

	public NonExistingCarException() {
		super();
	}

	public NonExistingCarException(Throwable t) {
		super();
	}

	public NonExistingCarException(Throwable t, String message) {
		super();
	}

	public NonExistingCarException(String message) {
		super();
	}

}
