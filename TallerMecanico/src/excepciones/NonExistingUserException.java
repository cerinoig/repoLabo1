package excepciones;

public class NonExistingUserException extends Exception {

	private static final long serialVersionUID = 1L;

	public NonExistingUserException() {
		super();
	}

	public NonExistingUserException(Throwable t) {
		super();
	}

	public NonExistingUserException(Throwable t, String message) {
		super();
	}

	public NonExistingUserException(String message) {
		super();
	}
}