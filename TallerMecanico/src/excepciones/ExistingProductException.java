package excepciones;

public class ExistingProductException extends Exception {
	
	private static final long serialVersionUID = 1L;

	public ExistingProductException() {
		super();
	}
	
	public ExistingProductException(Throwable t) {
		super();
	}
	
	public ExistingProductException(Throwable t, String message) {
		super();
	}
	
	public ExistingProductException(String message) {
		super();
	}

}
