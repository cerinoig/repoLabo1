package excepciones;

public class TallerMecanicoException extends Exception {

	public TallerMecanicoException() {
	}

	public TallerMecanicoException(String arg0) {
		super(arg0);
	}

	public TallerMecanicoException(Throwable arg0) {
		super(arg0);
	}

	public TallerMecanicoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TallerMecanicoException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
